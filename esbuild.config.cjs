const esbuild = require('esbuild');
const { copyFileSync, mkdirSync, readFileSync, writeFileSync } = require('fs');
const path = require('path');

mkdirSync('dist', { recursive: true });

esbuild.build({
  entryPoints: ['src/infrastructure/gas/index.ts'],
  bundle: true,
  outfile: 'dist/bundle.gs',
  format: 'iife',
  globalName: '__GAS__',
  target: 'es2019',
  platform: 'neutral',
  minify: false,
  sourcemap: false,
  external: ['pg', 'hono', 'dotenv'],
  tsconfig: 'tsconfig.json',
}).then(() => {
  // Post-process: GAS needs top-level function declarations, not IIFE.
  // We wrap the IIFE output and re-export exposed functions as top-level.
  let code = readFileSync('dist/bundle.gs', 'utf-8');

  // Remove "use strict" (GAS doesn't need it)
  code = code.replace(/^"use strict";\n?/m, '');

  const banner = [
    '// Auto-generated by esbuild. DO NOT EDIT.',
    '// Source: src/infrastructure/gas/index.ts',
    '',
  ].join('\n');

  // GAS top-level function stubs that delegate to the IIFE-exposed globalThis assignments
  const gasExports = `

// ============================================
// GAS Top-Level Functions
// These must be declared at top-level for GAS to discover them.
// The IIFE above assigns implementations to globalThis.
// ============================================

function doGet(e) { return globalThis.doGet(e); }
function getProducts() { return globalThis.getProducts(); }
function createProduct(input) { return globalThis.createProduct(input); }
function recordStockIn(input) { return globalThis.recordStockIn(input); }
function recordStockOut(input) { return globalThis.recordStockOut(input); }
function getStockSummary() { return globalThis.getStockSummary(); }
function getStockMovements(productId) { return globalThis.getStockMovements(productId); }
`;

  writeFileSync('dist/bundle.gs', banner + code + gasExports);

  copyFileSync(
    path.resolve('src/appsscript.json'),
    path.resolve('dist/appsscript.json')
  );
  console.log('✅ Server build complete: dist/bundle.gs');
}).catch((err) => {
  console.error('❌ Build failed:', err);
  process.exit(1);
});
