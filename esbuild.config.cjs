const esbuild = require('esbuild');
const { copyFileSync, mkdirSync } = require('fs');
const path = require('path');

mkdirSync('dist', { recursive: true });

esbuild.build({
  entryPoints: ['src/infrastructure/gas/index.ts'],
  bundle: true,
  outfile: 'dist/bundle.gs',
  format: 'iife',
  target: 'es2019',
  platform: 'neutral',
  minify: false,
  sourcemap: false,
  external: ['pg', 'hono', 'dotenv'],
  banner: {
    js: '// Auto-generated by esbuild. DO NOT EDIT.\n// Source: src/infrastructure/gas/index.ts\n'
  },
  tsconfig: 'tsconfig.json',
}).then(() => {
  copyFileSync(
    path.resolve('src/appsscript.json'),
    path.resolve('dist/appsscript.json')
  );
  console.log('✅ Server build complete: dist/bundle.gs');
}).catch((err) => {
  console.error('❌ Build failed:', err);
  process.exit(1);
});
